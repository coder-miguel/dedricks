          <h2>About Dedricks</h2>
          <p> Dedrick’s Farm Market started over 40 years ago at the corner it still sits on today. It began as a seasonal business that offered fresh produce through the spring and summer months. As time progressed, the business grew to include a variety of bedding plants and perennials. Greenhouses were built to accommodate growing plants and gave the business the ability to sell plants in the spring, fruits and veggies in the summer, and apples and fall crops during autumn. Last year the business was passed down to the next generation. With the change of hands came changes within the business. We are now open year round!

          The market is moving inside the old stone building for the winter months where we offer many local grocery and whole food items. We are proud to offer the following local products: milk, eggs, bread, cheese, goat milk products and coffee.  We also are building a large selection of baking items such as several choices of flours, black and natural cocoa, many types of sugar including raw and brown, a variety of oat products, spices and nuts.  We have miscellaneous items such as candy, chocolate covered goodies, homemade pastries on the weekends and more.

          We also offer country décor, as well as locally crafted pottery, jewelry and candles. There is a little bit of everything inside.

          Christmas trees and wreaths will be here starting Friday, November 27th. We will be selling our trees in our greenhouse to offer snow free trees throughout the season. See our add for the Christmas tree coupon.

          It is our mission at Dedrick’s Farm Market to be a place where each of our customers feel acknowledged and appreciated. Our goal is to provide a diversity of quality products that meet the needs and expectations of our customers. </p>

